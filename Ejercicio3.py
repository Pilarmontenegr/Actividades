# Validar Período: Desarrollar un formulario para validar un período con un formato de dos lugares para el mes, guión separador y 4 lugares para el año.
#1- Centrar el formulario.
#2- Insertar un control entry.
#3- Limitar el entry a 7 caracteres máximo.
#4- Permitir solo el ingreso de números, guión y/o teclas de control.
#5- Verificar que el mes esté entre 01 y 12, si no cumple mostrar la leyenda “El mes no es válido”.
#6- Verificar que el año mayor o igual a 1990 y menor o igual a 2022, si no cumple mostrar la leyenda “El año no es válido”.
#7- Si la validación resulta exitosa, entonces mostrar la leyenda “El período es válido”.